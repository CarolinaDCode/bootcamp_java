package clase_13_02;

public class Arquero extends PersonajeBase{

    private String tipoHabilidad;
    private int fuerzaHabilidad;

    public String getTipoHabilidad() {
        return tipoHabilidad;
    }

    public void setTipoHabilidad(String tipoHabilidad) {
        this.tipoHabilidad = tipoHabilidad;
    }

    public int getFuerzaHabilidad() {
        return fuerzaHabilidad;
    }

    public void setFuerzaHabilidad(int fuerzaHabilidad) {
        this.fuerzaHabilidad = fuerzaHabilidad;
    }
}
